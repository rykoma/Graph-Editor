        public static List<MicrosoftGraphScope> CustomScopes
        {
            get
            {
                var customScopes = GraphEditorApplication.GetSetting<string>(GraphEditorApplication.Settings.GlobalSetting_CustomScopes, string.Empty);

                if (string.IsNullOrEmpty(customScopes))
                {
                    return [];
                }
                else
                {
                    // Split the custom scopes string into an array, and create MicrosoftGraphScope objects
                    return customScopes.Split(',').Select(x => new MicrosoftGraphScope(x.Trim(), false, true)).ToList();
                }
            }
        }

        public static List<MicrosoftGraphScope> WellKnownAndCustomScopes
        {
            get
            {
                // Combine the well-known and custom scopes, remove duplicates, and sort the list
                List<MicrosoftGraphScope> allScopes = [.. WellKnownScopes, .. CustomScopes];
                allScopes = allScopes
                    .GroupBy(scope => scope.Name)
                    .Select(group => group.First())
                    .ToList();
                allScopes.Sort((x, y) => string.Compare(x.Name, y.Name, StringComparison.OrdinalIgnoreCase));
                return allScopes;
            }
        }

        internal static void SaveCustomScopes(List<MicrosoftGraphScope> filteredScopes)
        {
            // Save the custom scopes to the settings
            GraphEditorApplication.SaveSetting(GraphEditorApplication.Settings.GlobalSetting_CustomScopes, string.Join(",", filteredScopes.Select(x => x.Name)));
        }
